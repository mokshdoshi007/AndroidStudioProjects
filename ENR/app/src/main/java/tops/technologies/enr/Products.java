package tops.technologies.enr;

public class Products {
    long Barcode;
    String Name;
    int Price;

    public long getBarcode() {
        return Barcode;
    }

    public void setBarcode(long barcode) {
        Barcode = barcode;
    }

    public String getName() {
        return Name;
    }

    public void setName(String name) {
        Name = name;
    }

    public int getPrice() {
        return Price;
    }

    public void setPrice(int price) {
        Price = price;
    }
}
